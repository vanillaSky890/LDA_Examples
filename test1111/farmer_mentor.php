				<h1 class="home">Welcome!<br><span>Beginning Farmer Mentorship Program</span></h1>

				<h4>
				    <u>	Mentorship Program</u><br><br>

Application is available for download at the bottom of this page.<br><br>


The Beginning Farmer Program of Kissan Mitra is excited to present the 2018 Mentorship Program.  Each year  10 beginning farmers with experienced growers who can help them work towards specific learning goals.  This program is geared towards beginning farmers who have enough experience to move into farm management/ownership roles and can benefit from the support and input of a seasoned grower.  Participants work with their mentors to make specific learning plans which are supplimented by group meetings, conferences and workshops lead by Kissan mitra.<br><br>


The KIssan mitra  Beginning Farmer Mentorship Program aims to help fill the education gap between apprentice/farm worker and independent farmer. By pairing mentees with experienced growers who are not their employers, the program enables aspiring farmers to gain farming experiences, skills, and perspectives that they may otherwise miss in their day-to-day work.


				</h4>
				<p>
					<b><u>PROGRAM BENEFITS</u></b><br><br>


Mentor Pairing: Mentees will be paired with mentors based on interest, goals and location.  Applicants can request or suggest mentors they are interested in working with or NOFA/Mass will find a strong mentor match.<br><br>

<b><u>Workshops and Conferences:</u></b> Acceptance to the program will give mentees access to the Winter Conference, Summer Conference and stipends to Advanced Grower and on-farm workshops hosted by NOFA/Mass in 2017.   Program participants are required to attend a program kick-off meeting at the Winter Conference and are expected to attend the mid-season program meeting at the Summer Conference.<br><br>


<b><u>Mentorship Structure:</u></b> At the Winter Conference, the Beginning Farmer Program Coordinator will facilitate goal setting and the structuring of the mentorship based on the individuals in each mentor relationship.  Mentees and mentors can choose communication strategies, set dates to visit each other’s farms and map out a plan for the year.<br><br>


<b><u>Support:</u></b> Mentees have access to the Beginning Farmer Program Coordinator who can help facilitate the development of learning plans and connect mentees to resources that aid in their professional development as farmers.  Mentees will also connect with other participants at the conferences to develop a network of beginning farmers with whom they can share information and resources.<br><br>


<b><u>PROGRAM DETAILS</u></b>

<b><u>Program Timeline:</u></b> January-December 2018<br><br>


<b><u>Application Deadline:</u></b> December 7, 2018<br><br>


<b><u>Major Events:</u></b> Winter Conference Kick-off meeting, January 14th, 2017 and Summer Conference mid-season meeting, August 2017.<br><br>


<b><u>Quarterly Reporting/Check-In:</u></b> Communicate with Beginning Farmer Program Coordinator once per season to update on the progress of the mentorship.  Complete bi-annual evaluations to update NOFA/Mass on the progress of your mentorship.<br><br>


<b><u>Program Cost:</u></b> $200, NOFA/Mass Membership required. To become a NOFA/Mass member and see all the benefits that membership offers, visit www.nofamass.org/membership. <br><br>


<b><u>WHO SHOULD APPLY</u></b>

 “A mentee who has specific learning goals and/or is someone who wants to explore another perspective into their individual farming interests.” 
- Recommendation from previous participant

Beginning Farmers who have some farming experience and are interested in deepening their knowledge or pursuing specific goals related to their agricultural career should apply.  Applicants do not need to have access to land or an established farm business to apply. 

 We are seeking participants dedicated to a year of professional development. <br><br>


 <b><u>Example Goals of Previous Years Mentees</u></b>

            • Develop a season extension plan for their farm
            • Deepen understanding of soil fertility management
            • Improved seedling production and greenhouse management
            • Raise pigs in conjunction with a vegetable operation
            • Develop a business plan for a new farm
            • General management assistance for new managers
            • Learn more about employee management, labor structures
            • Explore the farm model and philosophy of their mentor farmer <br><br>




<b><u>INFORMATION FOR POTENTIAL MENTORS</u></b>

The decision to become a mentor should be made carefully with the understanding that as a mentor you will be expected to share some of your time and knowledge with a developing farmer. Our hope is that the process and relationship will prove rewarding for both the mentee and the mentor.

“[Being a Mentor] makes you work harder to make your own farm beautiful and productive to show off to your mentee! Also, you get to compare and contrast how things are growing on different sites based on different conditions. Finally, [being a mentor] helps build a community of people interested in local food production.”
– Past Mentor

EXPECTATIONS <br><br>


NOFA/Mass expects mentors to: <br><br>

Make a 12-month commitment to be a mentor.
Give approximately 15 hours over the course of the 12-month program to the mentee with whom they are paired. (More contact is encouraged, if appropriate.)
Share advice, technical assistance, wisdom, philosophies and sympathy with diligence and honesty, to the best of the mentor's ability, via phone, email and face-to-face meetings.
Host the mentee at your farm at least once during the course of the program.
Attend the program kick-off meeting at the NOFA/Mass Winter Conference (January 16, 2016 – Worcester) with all other mentor and mentee participants.
<br><br>
<b><u>BENEFITS</u></b>

In recognition of the important service that the mentor provides, NOFA/Mass agrees to provide each mentor an honorarium of $200 and a $35 discount on the registration fee for the Winter Conference. 
<br><br>
In coordination with other NOFA chapters, NOFA/Mass will also invite mentors to an annual get-together with other mentors from around the region. This will be an opportunity for veteran farmers who are committed to educating the next generation of growers to meet and share knowledge, experiences, and support.
<br><br>
MENTOR EXPERIENCE

If you are interested in mentoring a beginning farmer and would consider being paired by our mentorship program, please contact Beginning Farmer Program Coordinator, Brittany Sidway Overshiner.
